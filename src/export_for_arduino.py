"""Export TFLite model and feature extraction assets as C headers for Arduino.

Generates all header files needed by the car_sound_classifier Arduino sketch:
  - model_data.h      : TFLite model as aligned C byte array
  - mel_filterbank.h   : Sparse mel filterbank weights
  - normalization.h    : Per-band z-score mean/std arrays
  - hann_window.h      : 512-point Hann window coefficients

Usage:
    python src/export_for_arduino.py --model m6 --tier 2 --method ptq
    python src/export_for_arduino.py --model m2 --tier 2 --method ptq
    python src/export_for_arduino.py --model m5 --tier 2 --method ptq
"""

import argparse
import os
import sys
from pathlib import Path

import librosa
import numpy as np

os.environ["TF_CPP_MIN_LOG_LEVEL"] = "3"
import tensorflow as tf

